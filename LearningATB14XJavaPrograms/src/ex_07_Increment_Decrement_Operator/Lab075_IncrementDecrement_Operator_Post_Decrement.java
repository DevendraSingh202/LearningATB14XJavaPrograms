package ex_07_Increment_Decrement_Operator;

public class Lab075_IncrementDecrement_Operator_Post_Decrement {

	public static void main(String[] args) {
		
		int a = 10;
		int result_post = a--;
		System.out.println(result_post); // 10 -> First Assign then decrease
		System.out.println(a); // 9
		

	}

}
