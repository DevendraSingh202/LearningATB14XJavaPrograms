package ex_07_Increment_Decrement_Operator;

public class Lab073_IncrementDecrement_Operator_Example_3 {

	public static void main(String[] args) {

		int a = 10;
		System.out.println(a++  +  ++a);
		System.out.println(a);
		
		// Divide & Conquer
		// A + B
		// A -> a++
		// B -> ++a
		
		// ERT Table
		// Line No  |  a  | Exp
		// 7  | 10  | NA
		// 8  | 11  | 10 + 12 -> 22
		// 9  | 12
		
		

	}

}
